CREATE TABLE IF NOT EXISTS TP_ESTRUCTURAS_FISICAS
(
	ID INT AUTO_INCREMENT PRIMARY KEY,
	NOMBRE VARCHAR(200),
    DIRECCION VARCHAR(200) NOT NULL,
    CIUDAD VARCHAR(200),
    FECHA_CREACION DATETIME DEFAULT NOW(),
    FECHA_ACTUALIZACION DATETIME DEFAULT NOW(),
    ID_DEPARTAMENTO INT,
    
    CONSTRAINT FK_ESTRUCRTURAS_FISICAS_DEPARTAMENTOS
    FOREIGN KEY (ID_DEPARTAMENTO) REFERENCES TR_DEPARTAMENTOS(ID)
    ON UPDATE CASCADE
    ON DELETE SET NULL    
);


CREATE TABLE IF NOT EXISTS TM_HISTORIAL
(
	ID BIGINT AUTO_INCREMENT PRIMARY KEY,
	FECHA_INGRESO DATETIME DEFAULT NOW(),
	FECHA_SALIDA DATETIME DEFAULT NULL,
    FECHA_CREACION DATETIME DEFAULT NOW(),
    FECHA_ACTUALIZACION DATETIME DEFAULT NOW(),
    ID_ESTRUCTURA_FISICA INT NOT NULL,
    ID_PERSONA_INGRESA INT NOT NULL,    
    ID_PERSONA_REGISTRA INT NOT NULL,
    
    CONSTRAINT FK_HISTORIAL_ESTRUCTURA_FISICA 
    FOREIGN KEY (ID_ESTRUCTURA_FISICA) REFERENCES TP_ESTRUCTURAS_FISICAS(ID),
    
    CONSTRAINT FK_HISTORIAL_PERSONA_INGRESA
    FOREIGN KEY (ID_PERSONA_INGRESA) REFERENCES TM_PERSONAS(ID),    
    
    CONSTRAINT FK_HISTORIAL_PERSONA_REGISTRA
    FOREIGN KEY (ID_PERSONA_REGISTRA) REFERENCES TM_PERSONAS(ID)
);