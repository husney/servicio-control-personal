CREATE TABLE IF NOT EXISTS TM_PERSONAS
(
	ID INT AUTO_INCREMENT PRIMARY KEY,
	NUMERO_IDENTIFICACION VARCHAR(100) NOT NULL,
    PRIMER_NOMBRE VARCHAR(50) NOT NULL,
    SEGUNDO_NOMBRE VARCHAR(50),
    PRIMER_APELLIDO VARCHAR(50),
    SEGUNDO_APELLIDO VARCHAR(50),
    FECHA_CREACION DATETIME DEFAULT NOW(),
    FECHA_ACTUALIZACION DATETIME DEFAULT NOW(),
    FECHA_NACIMIENTO DATETIME,
    EDAD INT,
    ID_TIPO_IDENTIFICACION INT,
    
    CONSTRAINT FK_PERSONAS_TIPOS_IDENTIFICACION
    FOREIGN KEY (ID_TIPO_IDENTIFICACION) REFERENCES TR_TIPOS_IDENTIFICACION(ID)
    ON UPDATE CASCADE
    ON DELETE SET NULL
    
);

